function love.conf(t)

    t.window.title = "Melissa" 

    t.window.width = 1280
    t.window.height = 720

    t.window.resizable = false
    t.window.minwidth = 640
    t.window.minheight = 360
	
    t.window.msaa = false
    t.window.fsaa = false
    t.window.vsync = false
    t.window.fullscreen = false
    t.window.fullscreentype = "exclusive" 

    t.identity = "Melissa"

end